public class MineBean {

    private int ima;
    private int str;

    public MineBean() {
    }

    public MineBean(int ima, int str) {
        this.ima = ima;
        this.str = str;
    }

    public int getIma() {
        return ima;
    }

    public void setIma(int ima) {
        this.ima = ima;
    }

    public int getStr() {
        return str;
    }

    public void setStr(int str) {
        this.str = str;
    }
}
